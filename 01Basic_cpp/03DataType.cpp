#include<iostream>
using namespace std;

int main()
{
    int a = 4;          //integer data type
    float b = 4.3f ;       //floating point data can store
    char c = 'T';         //Single character can store
    string d = "trupti" ;   //sequence or word can store

    cout<<"integer = "<<a<<endl;
    cout<<"Float = "<<b<<endl;
    cout<<"Character = "<<c<<endl;
    cout<<"String = "<<d<<endl;
    return 0;
}